#!/usr/bin/python3
# Assign string to variable 'word'
word = "Holberton"

# Get first 3 characters of string
word_first_3 = word[:3]

# Get the last 2 characters of string
word_last_2 = word[-2:]

# Get string without first and last characters
middle_word = word[1:-1]

#Print results
print(f"First 3 letters: {word_first_3}")
print(f"Last 2 letters: {word_last_2}")
print(f"Middle word: {middle_word}")
